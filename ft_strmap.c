#include "libft.h"
#include <stdlib.h>

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	i;
	size_t	length;
	char	*ptr;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	length = ft_strlen(s);
	ptr = (char *)malloc(sizeof(ptr) * (length + 1));
	if (ptr == NULL)
		return (NULL);
	while (s[i])
	{
		ptr[i] = f(s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
